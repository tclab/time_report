    /**
     * Crear nueva tarea
     * @param evt
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //Propiedades de la tarea.
        String nombre = jTextField1.getText();
        String feInicio;
        if (jCheckBox1.isSelected()) {
            feInicio = new Utils().fechaActual();
        } else {
            int mes = jComboBox2.getSelectedIndex();
            feInicio = jComboBox1.getSelectedItem().toString() + "-" + mes +
                    "-" + jComboBox3.getSelectedItem().toString();
        }

        tarea = new TareaBO(nombre, proyecto);
        tarea.setTiempo("0");
        tarea.setFeInicio(feInicio);
        tarea.setComentario(jTextArea1.getText());

        tareasDao.nuevaTarea(tarea);

        index.llenarTareas();
        index.llenarProyetos();
        index.setProyectoSeleccionado(filaSeleccionada);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

